package chapter_four_使用执行器.运行多个任务并处理所有结果;

public class Result {
	private String name;
	private int value;

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getValue() {
		return this.value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
